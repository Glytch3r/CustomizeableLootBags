function CustomizeableLootBags.setLootRecord(bag, shouldRemove, recorder)
    if CustomizeableLootBags.isBag(bag) then
        local showTip = SandboxVars.CustomizeableLootBags.ShowContentsTooltip or true
        local pl = getPlayer()
        local inv = pl:getInventory()
        local ico = bag:getTexture():getName()
        local dName = bag:getDisplayName()

        recorder:getModData()['LootBagData'] = {}
        recorder:getModData()['LootBagData'].strList = tostring(CustomizeableLootBags.getStrList(bag))
        recorder:getModData()['LootBagData'].fType = bag:getFullType()
        recorder:getModData()['LootBagData'].ico = tostring(ico)
        recorder:getModData()['LootBagData'].dName = tostring(dName)

        recorder:setName(dName)
        CustomizeableLootBags.setIcon(recorder, ico)

        if showTip then
            recorder:setTooltip("Spawns: "..tostring(CustomizeableLootBags.getStrList(bag)))
        end

        if shouldRemove then inv:DoRemoveItem(bag) end
    else
        print("Err: Invalid Item")
    end
end