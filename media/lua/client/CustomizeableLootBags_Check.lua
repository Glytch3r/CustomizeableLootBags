function CustomizeableLootBags.isInsideInventoryContainer(ref)
    local cont = ref:getContainer():getContainingItem()
    local inv = getPlayer():getInventory()
    if cont and cont ~= inv then
        if instanceof(cont, "InventoryContainer") then
            return true
        elseif instanceof(cont, "IsoObject") then
            print("Container is an IsoObject or similar: " .. tostring(cont))
        else
            print("Container is of an unknown type: " .. type(cont))
        end
    end
    return false
end

function CustomizeableLootBags.isCanOpen()
	local reqItem = SandboxVars.CustomizeableLootBags.RequiredItem or "Base.CreditCard"
	local inv = getPlayer():getInventory()
	return inv:FindAndReturn(reqItem)
end